public class autoIncrementAndAutoDecrementOperators {
    public static void main(String args[]){
        int num1=100;
        int num2=225;
        num1++;
        //num++ is equivalent to num=num+1;
        System.out.println("num1++ is:"+num1);

        num2--;
        //num-- is equivalent to num=num-1;
        System.out.println("num2-- is:"+num2);



    }
}
